import java.util.*;



class TestClass {
    public static void main(String args[] )
    {
        Scanner aa=new Scanner(System.in);
         String a=aa.nextLine();
        int l=a.length();
        String rev="";
        for(int i=l-1;i>=0;i--)
        
           rev=rev+a.charAt(i);
        
           if (a.equals(rev))
           
               System.out.println("YES");
           
           else
           
               System.out.println("NO");
    }
}




